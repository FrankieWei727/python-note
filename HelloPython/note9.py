# For loop
for item in ['xuan','yu']:
    print(item)
print('-' * 30)

for item in 'python':
    print(item)
print('-' * 30)

for item in range(5,10):
    print(item)
print('-' * 30)

for item in range(5,10,2):
    print(item)
print('-' * 30)

# Nested Loops
for x in range(4):
    for y in range(4):
        print(f'({x},{y})')

