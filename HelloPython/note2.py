# Strings
course = "Python's course for beginners"
print(course[0])

course = 'Python for "beginners"'
print(course[-1])

course = '''
Hi,
I LOVE YOU!!!

From Xuan
'''
print(course)

# return the characters from the index 0 to 2
course = 'Python'
print(course[0:3])

# return all the characters
course = 'Python'
print(course[0:])

# exclude the first character
course = 'Python'
print(course[1:])

# return the characters from the index 0 to 4
# 0 as the start index
course = 'Python for beginners'
print(course[:5])

# copy the value
course = 'Python for beginners'
another = course[:]
print(another)

# start form the index 1 and exclusive the first
# character in the end
name = 'qinqianyu'
print(name[1:-1])







